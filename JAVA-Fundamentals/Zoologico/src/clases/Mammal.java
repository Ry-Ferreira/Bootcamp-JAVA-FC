package clases;

public class Mammal {
	
	private int energyLevel;
	
	public Mammal() {
		this.energyLevel = 100;
	}

	public int displayEnergy() {
		return energyLevel;
	}

	public int getEnergia() {
		return energyLevel;
	}

	public void setEnergia(int energia) {
		this.energyLevel = energia;
	}
	
	
}
